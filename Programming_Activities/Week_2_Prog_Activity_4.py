user_year = eval(input("What year where you born?"))

if user_year >= 1997:
    print("You are in the Zoomer Generation 1997")
elif user_year >= 1981:
    print("You are in the Millenial Generation 1981")
elif user_year >= 1965:
    print("You are in the Gen X Generation 1965")
elif user_year >= 1946:
    print("You are in the Baby Boomer Generation 1946")

